    /**
     * Botao "Salvar".
     * Captura os dados preenchidos, cria um objeto Aluno,
     * adiciona o aluno na tabela da tela de alunos e salva no arquivo.
     * @param evt 
     */
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Define o sexo
        if (rdMasculino.isSelected()) sexo = 'M';
        if (rdFeminino.isSelected()) sexo = 'F';
        
        // Transforma a data de String para LocalDate
        String dataStr = txtData.getText();
        DateTimeFormatter fmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNasc = LocalDate.parse(dataStr, fmt);
        
        // Concatena o endereco
        String enderecoCompleto = txtRua.getText() + ", " + txtNumero.getText() + ", " + txtBairro.getText() 
                + ", " + txtCidade.getText() + " - " + cmbEstado.getSelectedItem() + ", CEP: " + txtCEP.getText();
        
        // Cria o objeto Aluno
        Aluno a = new Aluno(txtNome.getText(), dataNasc, sexo, txtMatricula.getText(), txtCurso.getText(), 
            txtCPF.getText(), enderecoCompleto, cmbEstado.getSelectedItem().toString(), txtTelefone.getText());
        
        // Adiciona na tabela da tela de alunos
        DefaultTableModel tabela = (DefaultTableModel) telaAlunos.getTabela().getModel();
        tabela.addRow(a.obterDados());
        
        // Salva no arquivo
        salvarAlunoEmArquivo(a);
    }//GEN-LAST:event_btnSalvarActionPerformed

