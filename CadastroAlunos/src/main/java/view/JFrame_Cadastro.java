    /**
     * Metodo de classe que salva os dados de um aluno em um arquivo de texto.
     * Cada campo do aluno e separado por virgula.
     * @param a objeto Aluno a ser salvo.
     */
    private void salvarAlunoEmArquivo(Aluno a) {
        String caminhoArquivo = "alunos.txt";
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(caminhoArquivo, true))) {
            Object[] dados = a.obterDados();
            // Concatena os dados com virgula
            String linha = "";
            for (int i = 0; i < dados.length; i++) {
                linha += dados[i].toString();
                if (i < dados.length - 1) 
                    linha += ",";
            }
            bw.write(linha);
            bw.newLine();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

