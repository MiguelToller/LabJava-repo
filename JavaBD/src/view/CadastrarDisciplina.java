    private void carregarProfessores() {
        try {
            DAO.DisciplinaDAO dDAO = new DAO.DisciplinaDAO();
            cmbProfessor.removeAllItems();

            for (beans.Professor p : dDAO.getProfessores()) {
                cmbProfessor.addItem(p);
            }
        } catch (Exception ex) {
            System.out.println("Erro ao carregar professores: " + ex.getMessage());
        }
    }

