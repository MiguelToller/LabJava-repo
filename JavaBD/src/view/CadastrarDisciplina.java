    private void limparFormulario() {
        txtNome.setText("");
        txtCargaHoraria.setText("");
        cmbProfessor.setSelectedIndex(0);
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        beans.Disciplina d = new beans.Disciplina();
        DAO.DisciplinaDAO dDAO = new DAO.DisciplinaDAO();

        // Verifica se todos os campos foram preenchidos    
        if (txtNome.getText().isBlank() || txtCargaHoraria.getText().isBlank() || cmbProfessor.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(null, "Todos os campos devem estar preenchidos", "ERRO", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            d.setNome(txtNome.getText());
            d.setCargaHoraria(Integer.parseInt(txtCargaHoraria.getText()));
            beans.Professor prof = (beans.Professor) cmbProfessor.getSelectedItem();
            d.setIdProfessor(prof.getId());
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro inesperado: " + e.getMessage(), "ERRO", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        dDAO.inserir(d);
        limparFormulario();
        javax.swing.JOptionPane.showMessageDialog(null, "Disciplina cadastrada com sucesso!");
    }//GEN-LAST:event_btnSalvarActionPerformed

