    private void limparFormulario(){
        txtNome.setText("");
        txtIdade.setText("");
        txtCurso.setText("");
    }
    
    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int idAluno = Integer.parseInt(txtId.getText());
        AlunoDAO aDAO = new AlunoDAO();
        
        Aluno a = aDAO.getAluno(idAluno);
        if (a == null) {
            txtNome.setText("");
            txtIdade.setText("");
            txtCurso.setText("");
        }
        else {
            txtId1.setText(String.valueOf(a.getId()));
            txtNome.setText(a.getNome());
            txtIdade.setText(String.valueOf(a.getIdade()));
            txtCurso.setText(a.getCurso());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Aluno a = new Aluno();
        a.setId(Integer.parseInt(txtId1.getText()));
        a.setNome(txtNome.getText());
        a.setIdade(Integer.parseInt(txtIdade.getText()));
        a.setCurso(txtCurso.getText());
        
        AlunoDAO aDAO = new AlunoDAO();
        aDAO.editar(a);
        limparFormulario();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            AlunoDAO aDAO = new AlunoDAO();
            aDAO.excluir(Integer.parseInt(txtId1.getText()));
            limparFormulario();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

