    private void limparFormulario(){
        txtNome.setText("");
        txtIdade.setText("");
        txtDisciplina.setText("");
    }
    
    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int idProfessor = Integer.parseInt(txtId.getText());
        ProfessorDAO pDAO = new ProfessorDAO();

        Professor p = pDAO.getProfessor(idProfessor);
        if (p == null) {
            txtNome.setText("");
            txtIdade.setText("");
            txtDisciplina.setText("");
        }
        else {
            txtId1.setText(String.valueOf(p.getId()));
            txtNome.setText(p.getNome());
            txtIdade.setText(String.valueOf(p.getIdade()));
            txtDisciplina.setText(p.getDisciplina());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Professor p = new Professor();
        p.setId(Integer.parseInt(txtId1.getText()));
        p.setNome(txtNome.getText());
        p.setIdade(Integer.parseInt(txtIdade.getText()));
        p.setDisciplina(txtDisciplina.getText());

        ProfessorDAO pDAO = new ProfessorDAO();
        pDAO.editar(p);
        limparFormulario();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            ProfessorDAO pDAO = new ProfessorDAO();
            pDAO.excluir(Integer.parseInt(txtId1.getText()));
            limparFormulario();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

